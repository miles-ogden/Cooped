/**
 * Get math questions filtered by difficulty level
 * @param {number} difficulty - Difficulty level (1-5)
 * @returns {Array} Array of math questions matching the difficulty
 */
export function getMathQuestionsByDifficulty(difficulty) {
  return mathQuestions.filter(q => q.difficulty === difficulty);
}

/**
 * Get a random math question by difficulty level
 * @param {number} difficulty - Difficulty level (1-5)
 * @returns {Object} A random math question of the specified difficulty
 */
export function getRandomMathQuestionByDifficulty(difficulty) {
  const filtered = getMathQuestionsByDifficulty(difficulty);
  if (filtered.length === 0) return getRandomMathQuestion();
  const randomIndex = Math.floor(Math.random() * filtered.length);
  return filtered[randomIndex];
}
