/**
 * Get a random history question
 * @returns {Object} A random history question object
 */
export function getRandomHistoryQuestion() {
  const randomIndex = Math.floor(Math.random() * historyQuestions.length);
  return historyQuestions[randomIndex];
}

/**
 * Get history questions filtered by difficulty level
 * @param {number} difficulty - Difficulty level (1-5)
 * @returns {Array} Array of history questions matching the difficulty
 */
export function getHistoryQuestionsByDifficulty(difficulty) {
  return historyQuestions.filter(q => q.difficulty === difficulty);
}

/**
 * Get a random history question by difficulty level
 * @param {number} difficulty - Difficulty level (1-5)
 * @returns {Object} A random history question of the specified difficulty
 */
export function getRandomHistoryQuestionByDifficulty(difficulty) {
  const filtered = getHistoryQuestionsByDifficulty(difficulty);
  if (filtered.length === 0) return getRandomHistoryQuestion();
  const randomIndex = Math.floor(Math.random() * filtered.length);
  return filtered[randomIndex];
}
