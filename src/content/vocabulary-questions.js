/**
 * Get a random vocabulary question
 * @returns {Object} A random question object
 */
export function getRandomVocabularyQuestion() {
  const randomIndex = Math.floor(Math.random() * vocabularyQuestions.length);
  return vocabularyQuestions[randomIndex];
}

/**
 * Get vocabulary questions by difficulty
 * @param {number} difficulty - Difficulty level (1-5)
 * @returns {Array} Array of questions matching the difficulty
 */
export function getVocabularyQuestionsByDifficulty(difficulty) {
  return vocabularyQuestions.filter(q => q.difficulty === difficulty);
}
